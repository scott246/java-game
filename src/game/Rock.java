public class Rock {
    static int size = 20;
    int x = 0;
    int y = 0;
    
    public int getSize() {
        return size;
    }
    public void setSize(int size1) {
        size = size1;
    }
    
    public int getX() {
        return x;
    }
    public void setX(int x1) {
        x = x1;
    }
    
    public int getY() {
        return y;
    }
    public void setY(int y1) {
        y = y1;
    }
    
}
